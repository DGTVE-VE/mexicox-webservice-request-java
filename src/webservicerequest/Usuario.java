package webservicerequest;

class Usuario {
        private String nombre;
        private String apellidos;
        private String curp;
        private String email;
        private String estado;
        private String anioNacimiento;
        private String genero;
        private String sobrenombre;
        private String password;
        private String ciudad;
        private String pais;
        private String nivelEstudios;
        private String codigoPostal;

        /**
         * @return the nombre
         */
        public String getNombre() {
            return nombre;
        }

        /**
         * @param nombre the nombre to set
         */
        public void setNombre(String nombre) {
            this.nombre = nombre;
        }

        /**
         * @return the curp
         */
        public String getCurp() {
            return curp;
        }

        /**
         * @param curp the curp to set
         */
        public void setCurp(String curp) {
            this.curp = curp;
        }

        /**
         * @return the email
         */
        public String getEmail() {
            return email;
        }

        /**
         * @param email the email to set
         */
        public void setEmail(String email) {
            this.email = email;
        }

        /**
         * @return the estado
         */
        public String getEstado() {
            return estado;
        }

        /**
         * @param estado the estado to set
         */
        public void setEstado(String estado) {
            this.estado = estado;
        }

        /**
         * @return the anioNacimiento
         */
        public String getAnioNacimiento() {
            return anioNacimiento;
        }

        /**
         * @param anioNacimiento the anioNacimiento to set
         */
        public void setAnioNacimiento(String anioNacimiento) {
            this.anioNacimiento = anioNacimiento;
        }

        /**
         * @return the genero
         */
        public String getGenero() {
            return genero;
        }

        /**
         * @param genero the genero to set
         */
        public void setGenero(String genero) {
            this.genero = genero;
        }

        /**
         * @return the sobrenombre
         */
        public String getSobrenombre() {
            return sobrenombre;
        }

        /**
         * @param sobrenombre the sobrenombre to set
         */
        public void setSobrenombre(String sobrenombre) {
            this.sobrenombre = sobrenombre;
        }

        /**
         * @return the password
         */
        public String getPassword() {
            return password;
        }

        /**
         * @param password the password to set
         */
        public void setPassword(String password) {
            this.password = password;
        }

        /**
         * @return the ciudad
         */
        public String getCiudad() {
            return ciudad;
        }

        /**
         * @param ciudad the ciudad to set
         */
        public void setCiudad(String ciudad) {
            this.ciudad = ciudad;
        }

        /**
         * @return the pais
         */
        public String getPais() {
            return pais;
        }

        /**
         * @param pais the pais to set
         */
        public void setPais(String pais) {
            this.pais = pais;
        }

        /**
         * @return the nivelEstudios
         */
        public String getNivelEstudios() {
            return nivelEstudios;
        }

        /**
         * @param nivelEstudios the nivelEstudios to set
         */
        public void setNivelEstudios(String nivelEstudios) {
            this.nivelEstudios = nivelEstudios;
        }

    /**
     * @return the apellidos
     */
    public String getApellidos() {
        return apellidos;
    }

    /**
     * @param apellidos the apellidos to set
     */
    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    /**
     * @return the codigoPostal
     */
    public String getCodigoPostal() {
        return codigoPostal;
    }

    /**
     * @param codigoPostal the codigoPostal to set
     */
    public void setCodigoPostal(String codigoPostal) {
        this.codigoPostal = codigoPostal;
    }
    }